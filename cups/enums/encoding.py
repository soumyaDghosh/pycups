    ISO8859_7 = _lib.CUPS_ENCODING_ISO8859_7
    ISO8859_8 = _lib.CUPS_ENCODING_ISO8859_8
    ISO8859_9 = _lib.CUPS_ENCODING_ISO8859_9
    JIS_X0213 = _lib.CUPS_ENCODING_JIS_X0213
